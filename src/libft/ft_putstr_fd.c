#include <unistd.h>

int	ft_putstr_fd(char *s, int fd)
{
	if (!s || !*s)
		return (0);
	while (*s != '\0')
	{
		if (write(fd, s, 1) < 0)
			return (1);
		s++;
	}
	return (0);
}
